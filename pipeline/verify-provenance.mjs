#!/usr/bin/env node
/**
 * Provenance verification gate - proves each shipped sprite is genuinely AI-generated.
 *
 * Plain-language: the other gates check a sprite is a valid, good-looking image. This one answers
 * a different question: "where did it come from?" When the pipeline generates a sprite it records,
 * in assets/provenance.json, which AI generator made it, the prompt's fingerprint, which AI graded
 * it, and the image's SHA-256 (a unique fingerprint of the exact bytes). This gate re-fingerprints
 * every committed sprite and checks it matches - so a hand-drawn or procedural placeholder swapped
 * in later would FAIL. It is an auditable chain of custody from prompt to shipped pixel.
 *
 * Honest scope: this proves the sprite came from THIS pipeline's AI generator and hasn't been
 * altered since. For third-party cryptographic proof of AI origin you'd additionally check C2PA
 * "content credentials" embedded by the generator (the natural next upgrade).
 *
 * Usage:  node pipeline/verify-provenance.mjs      Exit 0 = all verified, 1 = a sprite failed.
 */

import { readdirSync, readFileSync, existsSync } from 'node:fs';
import { join, dirname, basename } from 'node:path';
import { fileURLToPath } from 'node:url';
import { createHash } from 'node:crypto';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const SPRITES = join(ROOT, 'assets', 'sprites');
const MANIFEST = join(ROOT, 'assets', 'provenance.json');

// Generators that count as AI image generation. A procedural 'mock' placeholder is NOT AI origin.
const AI_GENERATORS = new Set(['pollinations', 'openai', 'stability', 'dalle', 'gemini-image']);
const sha = (buf) => createHash('sha256').update(buf).digest('hex');

if (!existsSync(MANIFEST)) {
  console.error('No assets/provenance.json. Generate sprites with: node pipeline/agentic/run.mjs --promote');
  process.exit(1);
}
const manifest = JSON.parse(readFileSync(MANIFEST, 'utf8'));
const pngs = readdirSync(SPRITES).filter((f) => f.endsWith('.png'));

if (pngs.length === 0) {
  console.log('No sprites in assets/sprites/ yet (using procedural placeholders).');
  process.exit(0);
}

let failed = 0;
for (const f of pngs) {
  const name = basename(f, '.png');
  const rec = manifest[name];
  const problems = [];
  if (!rec) {
    problems.push('no provenance record (origin unknown)');
  } else {
    if (sha(readFileSync(join(SPRITES, f))) !== rec.imageSha256) {
      problems.push('SHA-256 mismatch - file changed since generation');
    }
    if (!AI_GENERATORS.has(rec.generator)) {
      problems.push(`generator "${rec.generator}" is not an AI image generator`);
    }
  }
  if (problems.length) {
    failed++;
    console.log(`FAIL  ${f}: ${problems.join('; ')}`);
  } else {
    console.log(
      `PASS  ${f}  AI-generated by ${rec.generator}, graded ${rec.judge} (${rec.score}/25), ` +
        `sha ${rec.imageSha256.slice(0, 12)}…`
    );
  }
}

console.log(`\n${pngs.length - failed}/${pngs.length} sprites have verified AI provenance.`);
process.exit(failed ? 1 : 0);
